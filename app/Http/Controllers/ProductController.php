class ProductController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $products = Product::cursor();
        return view('admin.products.index', compact('products'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $markets = Market::pluck('id', 'name')->prepend('', 'Please select');
        return view('admin.products.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  App\Http\Requests\StoreProductRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreProductRequest $request)
    {
        Product::create($request->validated());

        $successMsg = trans('global.messages.success.create', ['name' => trans('cruds.products.title_singular')]);
        return redirect()->route('admin.products.index', app()->getLocale())->withSuccess($successMsg);
    }

